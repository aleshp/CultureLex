  const catWords = getWordsByCategory(categoryId)
  const pool = [...catWords].sort(() => Math.random() - 0.5).slice(0, 6)

  return pool.map((word) => {
    const type = Math.random() > 0.5 ? 'en_to_kk' : 'kk_to_en'
    const wrongPool = [...words].filter((w) => w.id !== word.id).sort(() => Math.random() - 0.5).slice(0, 3)

    if (type === 'en_to_kk') {
      const options = [word.kazakh, ...wrongPool.map((w) => w.kazakh)].sort(() => Math.random() - 0.5)
      return { wordId: word.id, type, question: word.english, options, correctIndex: options.indexOf(word.kazakh) }
    } else {
      const options = [word.english, ...wrongPool.map((w) => w.english)].sort(() => Math.random() - 0.5)
      return { wordId: word.id, type, question: word.kazakh, options, correctIndex: options.indexOf(word.english) }
    }
  })
